    private void btGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGrabarActionPerformed
               String descripcion = txtDescripcion.getText();
        String color = txtColor.getText();
        String talla = txtTalla.getText();
        float precio = Float.parseFloat(txtPrecio.getText());
        
        camisa objCamisa= new camisa(1,descripcion,talla,color,precio );
        objCamisa.GuardarCamisa();
        JOptionPane.showMessageDialog(null, "Registro guardado!!");

        ConsultarCamisas();
    }//GEN-LAST:event_btGrabarActionPerformed
private void tablaCamisasMouseClicked(java.awt.event.MouseEvent evt) {                                          
int fila = tablaCamisas.getSelectedRow();
DefaultTableModel model= (DefaultTableModel)tablaCamisas.getModel();
lblId.setText(model.getValueAt(fila, 0).toString());
txtDescripcion.setText(model.getValueAt(fila, 1).toString());
txtColor.setText(model.getValueAt(fila, 2).toString());
txtTalla.setText(model.getValueAt(fila, 3).toString());
txtPrecio.setText(model.getValueAt(fila, 4).toString());
 }
    private void ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarActionPerformed
        int id = Integer.parseInt(lblId.getText());
        String descripcion = txtDescripcion.getText();
        String color = txtColor.getText();
        String talla = txtTalla.getText();
        float precio = Float.parseFloat(txtPrecio.getText());
        
        camisa objCamisa= new camisa(id,descripcion,talla,color,precio );
        objCamisa.EliminarCamisa();
        JOptionPane.showMessageDialog(null, "Registro eliminado!!");

        ConsultarCamisas();
    }//GEN-LAST:event_ActualizarActionPerformed

